package com.dbit.datatypes;

public class Demo //Primitive Data Types
{
	public static void main(String[] args) //main method
	{
		System.out.println(Byte.MIN_VALUE+"----->"+Byte.MAX_VALUE);//min and max value for  byte
		System.out.println(Short.MIN_VALUE+"---->"+Short.MAX_VALUE);// min and max value for short
		System.out.println(Integer.MIN_VALUE+"----->"+Integer.MAX_VALUE); //min and max value for integer
		System.out.println(Long.MIN_VALUE+"----->"+Long.MAX_VALUE); //min and max value for long
		System.out.println(Float.MIN_VALUE+"----->"+Float.MAX_VALUE); //min and max value for float
		System.out.println(Double.MIN_VALUE+"----->"+Double.MAX_VALUE); //min and max value for double
		System.out.println(Character.MIN_VALUE+"----->"+Character.MAX_VALUE); //min and value for char
		//System.out.println(Boolean.MIN_VALUE+"----->"+Boolean.MAX_VALUE);---> Error
	}

}
