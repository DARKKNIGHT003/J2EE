package com.dbit.operators;

public class Demo1 // DECREMENTATION
{
	public static void main(String[] args) 
	{
		
		int a = 7;

		System.out.println(a);		//o/p--> 7
		System.out.println(a--);	//o/p--> 7 ,7 --, -->6 
		System.out.println(a);		//o/p--> 6
		System.out.println(--a);       // 6--> 6 -- -> 5 o/p--> 5
		System.out.println(a);         // o/p--> 5
		
	}

}
