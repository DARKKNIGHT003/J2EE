package com.dbit.operators;

public class Demo2 // DECREMENTATION
{
	public static void main(String[] args) 
	{
		int a = 7;

		System.out.println(a);				// --> 7
		System.out.println(++a);   			// --> 8
		System.out.println(a); 				// --> 8
		System.out.println(a++); 			// --> 8   -->9
		System.out.println(--a); 			// --> 9->8  8
		System.out.println(a); 				// --> 8
	}

}
