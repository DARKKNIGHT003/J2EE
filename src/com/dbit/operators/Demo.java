package com.dbit.operators;

public class Demo //INCREMENTATION
{
	public static void main(String[] args) 
	{
		int a = 7;

		System.out.println(a);		//	o/p--> 7
		System.out.println(++a);	 //7---> ++ 8, o/p--> 8
		System.out.println(a);			// o/p--> 8
		System.out.println(a++);	// 0/p--> 8  8--> 8++ -->9
		System.out.println(a);		//	o/p--> 9
		
	}

}
